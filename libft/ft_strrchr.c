char	*ft_strrchr(const char *s, int c)
{
	char *ptr;

	ptr = (char *)s;
	while (*s++)
	{
	}
	while (--s != ptr && *s != c)
	{
	}
	if (*s == c)
	{
		return ((char *)s);
	}
	return (0);
}
